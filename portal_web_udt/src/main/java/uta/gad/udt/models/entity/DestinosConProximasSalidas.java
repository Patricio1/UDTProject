package uta.gad.udt.models.entity;

public class DestinosConProximasSalidas {
	public String nombreOrigen;
	public String nombreDestino;
	public Long idOrigen;
	public Long idDestino;
	public String imgDestino;
	public String getImgDestino() {
		return imgDestino;
	}
	public void setImgDestino(String imgDestino) {
		this.imgDestino = imgDestino;
	}
	public String getNombreOrigen() {
		return nombreOrigen;
	}
	public void setNombreOrigen(String nombreOrigen) {
		this.nombreOrigen = nombreOrigen;
	}
	public String getNombreDestino() {
		return nombreDestino;
	}
	public void setNombreDestino(String nombreDestino) {
		this.nombreDestino = nombreDestino;
	}
	public Long getIdOrigen() {
		return idOrigen;
	}
	public void setIdOrigen(Long idOrigen) {
		this.idOrigen = idOrigen;
	}
	public Long getIdDestino() {
		return idDestino;
	}
	public void setIdDestino(Long idDestino) {
		this.idDestino = idDestino;
	}
	
	
}
